function normalizeLocale(locale) {
  const raw = String(locale || "").toLowerCase();
  if (raw.startsWith("en")) return "en";
  if (raw.startsWith("es")) return "es";
  return "pt-BR";
}

function translateLoose(text, locale) {
  const target = normalizeLocale(locale);
  if (target === "pt-BR") return text;
  let out = String(text || "");
  for (const [pattern, replacement] of REPLACEMENTS[target] || []) {
    out = out.replace(pattern, replacement);
  }
  return out;
}

export function localizeExamQuestion(question, locale) {
  const target = normalizeLocale(locale);
  if (target === "pt-BR") return question;
  return {
    ...question,
    subjectLabel: SUBJECT_LABELS[target]?.[question.subjectLabel] || question.subjectLabel,
    topic: TOPIC_LABELS[target]?.[question.topic] || translateLoose(question.topic, target),
    question: translateLoose(question.question, target),
    options: (question.options || []).map((option) => translateLoose(option, target)),
    explanation: translateLoose(question.explanation, target),
    reference: translateLoose(question.reference, target),
  };
}

export function localizeExamScore(score, locale) {
  const target = normalizeLocale(locale);
  if (target === "pt-BR" || !score) return score;
  return {
    ...score,
    bySubject: (score.bySubject || []).map((subject) => ({
      ...subject,
      label: SUBJECT_LABELS[target]?.[subject.label] || subject.label,
    })),
    questions: (score.questions || []).map((question) => localizeExamQuestion(question, target)),
  };
}
